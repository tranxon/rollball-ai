/// Source of a retrieval result.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ResultSource {
    /// Direct match from hybrid search.
    DirectMatch,
    /// Expanded via graph diffusion.
    GraphExpansion,
}

/// Context source for memory injection priority.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ContextSource {
    /// Autobiographical memory (never trimmed).
    Autobiographical,
    /// Core semantic facts.
    SemanticCore,
    /// Procedural behavior patterns.
    Procedural,
    /// User preferences.
    UserPreference,
    /// Learned from failures.
    FailureLesson,
    /// Graph expansion results.
    GraphExpansion,
    /// Episodic layer.
    Episodic,
}

/// A retrieval result with metadata.
#[derive(Debug, Clone)]
pub struct SearchResult {
    /// Memory node content.
    pub content: String,
    /// Label of the memory node.
    pub label: String,
    /// Relevance score [0.0, 1.0].
    pub score: f64,
    /// How this result was obtained.
    pub source: ResultSource,
    /// Estimated token count for context injection.
    pub context_tokens: usize,
    /// Node ID for tracing.
    pub node_id: u64,
    /// Source context for conflict detection.
    pub source_context: Option<String>,
}

/// Memory context for injection into LLM prompts.
#[derive(Debug, Clone)]
pub struct MemoryContext {
    /// Formatted memory content.
    pub content: String,
    /// Injection priority (0 = highest, 7 = lowest).
    pub priority: u8,
    /// Which cognitive layer this came from.
    pub source: ContextSource,
    /// Estimated token count.
    pub estimated_tokens: usize,
}

