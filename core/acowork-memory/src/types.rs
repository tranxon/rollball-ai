/// Hint type for retrieval strategy (from memory_hint.type).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Default)]
pub enum HintType {
    /// Semantic association (s) - broader retrieval
    #[default]
    Semantic,
    /// Factual recall (f) - precise retrieval
    Factual,
    /// Relational diffusion (r) - graph expansion
    Relational,
    /// Identity/preference (i) - autobiographical focus
    Identity,
}

impl HintType {
    pub fn as_str(&self) -> &'static str {
        match self {
            HintType::Semantic => "s",
            HintType::Factual => "f",
            HintType::Relational => "r",
            HintType::Identity => "i",
        }
    }
}

impl std::str::FromStr for HintType {
    type Err = String;
    fn from_str(s: &str) -> std::result::Result<Self, Self::Err> {
        match s {
            "s" => Ok(HintType::Semantic),
            "f" => Ok(HintType::Factual),
            "r" => Ok(HintType::Relational),
            "i" => Ok(HintType::Identity),
            _ => Err(format!("unknown HintType: {s}")),
        }
    }
}

/// Filter for node type selection.
#[derive(Debug, Clone, PartialEq, Eq, Serialize, Deserialize)]
pub enum NodeTypeFilter {
    /// Episodic layer nodes.
    Episodic,
    /// Knowledge nodes (semantic layer).
    Knowledge,
    /// Procedural nodes (semantic layer).
    Procedural,
    /// Autobiographical nodes.
    Autobiographical,
}

/// Memory query parameters (replaces raw `&str` for future extensibility).
#[derive(Debug, Clone)]
pub struct MemoryQuery {
    /// Text query for hybrid search.
    pub query_text: String,
    /// Optional filters.
    pub filters: MemoryFilters,
    /// Maximum number of results.
    pub limit: usize,
    /// Graph expansion hops (0 = no expansion).
    pub expand_hops: u8,
    /// Minimum relevance score threshold.
    pub min_score: Option<f32>,
    /// Enable abstention (return empty if confidence too low).
    pub abstention_enabled: bool,
    /// Hint type for retrieval strategy optimization.
    pub hint_type: HintType,
    /// Pre-computed embedding vector (generated by Runtime).
    pub embedding: Option<Vec<f32>>,
}

impl MemoryQuery {
    /// Create a new memory query with default settings.
    pub fn new(query_text: impl Into<String>) -> Self {
        Self {
            query_text: query_text.into(),
            filters: MemoryFilters::default(),
            limit: 10,
            expand_hops: 0,
            min_score: None,
            abstention_enabled: false,
            hint_type: HintType::default(),
            embedding: None,
        }
    }

    /// Build a query for per-turn auto-injection.
    ///
    /// Lightweight background context: searches Autobiographical + Episodic
    /// labels, excludes current-session nodes, no graph expansion, low limit.
    pub fn auto_inject(
        query_text: String,
        exclude_session_id: Option<String>,
    ) -> Self {
        Self {
            query_text,
            embedding: None,
            filters: MemoryFilters {
                exclude_session_id,
                ..Default::default()
            },
            limit: 5,
            expand_hops: 0,
            min_score: Some(0.3),
            abstention_enabled: false,
            hint_type: HintType::Identity,
        }
    }

    /// Build a query for LLM-triggered deep recall (memory_recall tool).
    ///
    /// Comprehensive retrieval: searches all four labels, enables graph
    /// expansion, no score filtering — lets the LLM decide relevance.
    pub fn deep_recall(
        query_text: String,
        exclude_session_id: Option<String>,
    ) -> Self {
        Self {
            query_text,
            embedding: None,
            filters: MemoryFilters {
                exclude_session_id,
                ..Default::default()
            },
            limit: 10,
            expand_hops: 2,
            min_score: None,
            abstention_enabled: false,
            hint_type: HintType::Semantic,
        }
    }
}

/// Filters for memory queries.
#[derive(Debug, Clone, Default)]
pub struct MemoryFilters {
    /// Filter by node types.
    pub node_types: Vec<NodeTypeFilter>,
    /// Filter by privacy levels.
    pub privacy_levels: Vec<PrivacyLevel>,
    /// Filter by time range.
    pub time_range: Option<(DateTime<Utc>, DateTime<Utc>)>,
    /// Filter by session ID (include only this session).
    pub session_id: Option<String>,
    /// Exclude nodes belonging to this session.
    ///
    /// Used by per-turn auto-injection to avoid re-injecting summaries
    /// that are already present in the current session's context window.
    pub exclude_session_id: Option<String>,
}

