//!
//! These types define the core data structures for the three-layer memory model:
//! - Episodic layer (Episode)
//! - Semantic layer (KnowledgeNode, ProceduralNode)
//! - Autobiographical layer (AutobiographicalNode)
//!
//! Also includes retrieval result types and storage configuration.

