/// Type of knowledge conflict detected.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum ConflictType {
    /// Knowledge has evolved (new info supersedes old).
    Evolution,
    /// Knowledge correction (new info contradicts old).
    Correction,
    /// Cannot determine relationship.
    Ambiguous,
}

/// Signal indicating potential knowledge conflict.
#[derive(Debug, Clone)]
pub struct ConflictSignal {
    /// Semantic similarity score.
    pub semantic_score: f32,
    /// Whether old and new are temporally conflicting.
    pub temporal_conflict: bool,
    /// Whether context suggests negation.
    pub context_negation: bool,
    /// Suggested conflict type.
    pub suggested_type: ConflictType,
    /// Heuristic confidence in detection [0.0, 1.0].
    pub heuristic_confidence: f32,
}

