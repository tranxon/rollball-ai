pub struct KnowledgeNode {
    /// Subject of the knowledge (usually "user").
    pub subject: String,
    /// Predicate / relation.
    pub predicate: String,
    /// Object / value.
    pub object: String,
    /// Sub-type classification.
    pub sub_type: KnowledgeSubType,
    /// Confidence [0.0, 1.0].
    pub confidence: f32,
    /// Source episode ID (traceability).
    pub source_episode_id: Option<u64>,
    /// Semantic embedding.
    pub embedding: Option<Vec<f32>>,
    /// Lifecycle status.
    pub status: NodeStatus,
    /// Creation timestamp.
    pub created_at: DateTime<Utc>,
    /// Last update timestamp.
    pub updated_at: DateTime<Utc>,
    /// Optional metadata.
    pub metadata: HashMap<String, serde_json::Value>,
}

/// Procedural memory node — behavior pattern.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct ProceduralNode {
    /// Human-readable name.
    pub name: String,
    /// Trigger condition description.
    pub trigger_condition: String,
    /// Action pattern description.
    pub action_pattern: String,
    /// Number of successful activations.
    pub success_count: u32,
    /// Number of failed activations.
    pub fail_count: u32,
    /// Confidence [0.0, 1.0].
    pub confidence: f32,
    /// How many times this procedure has been activated (retrieved + injected).
    #[serde(default)]
    pub activation_count: u32,
    /// Which skill this procedure was learned from (if applicable).
    #[serde(default)]
    pub source_skill: Option<String>,
    /// How this procedure was learned: "user_feedback", "execution_failure",
    /// "offline_consolidation", "generalization", or "unknown".
    #[serde(default = "default_learned_from")]
    pub learned_from: String,
    /// Semantic embedding.
    pub embedding: Option<Vec<f32>>,
    /// Lifecycle status.
    pub status: NodeStatus,
    /// Creation timestamp.
    pub created_at: DateTime<Utc>,
    /// Last update timestamp.
    pub updated_at: DateTime<Utc>,
    /// Optional metadata.
    pub metadata: HashMap<String, serde_json::Value>,
}

fn default_learned_from() -> String {
    "unknown".to_string()
}

